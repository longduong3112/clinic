<p>Tư vấn, khám bệnh từ xa được thực hiện dưới hình thức gọi video, 1 bác sĩ hoặc 2 bác sĩ hoặc nhiều hơn, tùy tình trạng bệnh và biểu hiện có liên quan đến nhiều chuyên khoa sẽ có sự hỗ trợ hợp lý. Người bệnh &nbsp;nói chuyện và trình bày tình trạng bệnh của mình trực tiếp với bác sĩ để được thăm hỏi kỹ càng về tiệu chứng bệnh cũng như nhìn sắc thái, hình ảnh biểu hiện bệnh lý bên ngoài để đánh giá, hổ trợ tốt hơn cho chẩn đoán. Khám bệnh từ xa không những đòi hỏi yêu cầu về bác sĩ có chuyên môn và kinh nghiệm cao mà cần có đội hỗ&nbsp;trợ hậu cần về hình ảnh, đường truyền kết nối, để đảm bảo thông tin tín hiệu truyền đạt giữa bệnh nhân và bác sĩ được diễn ra thông suốt, thuận lợi. Sau khi thăm khám đánh giá và đưa ra chẩn đoán bác sĩ&nbsp;sẽ kê toa thuốc và chuyển thuốc đến nhà để bệnh nhân điều trị theo liệu trình đã hướng dẫn trong buổi khám bệnh. Cũng có một số bệnh cần thăm khám trực tiếp và cần theo dõi kỹ hơn, Bác sĩ sẽ hướng dẫn vào viện để khám và kiểm tra theo từng trường hợp cụ thể.&nbsp;</p>


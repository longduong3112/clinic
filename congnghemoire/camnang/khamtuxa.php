-&nbsp;&nbsp; &nbsp;Cuộc gọi video khám bệnh có thể kết thúc sớm hơn thời gian dự kiến nếu bác sĩ cảm thấy đã tư vấn đầy đủ cho người bệnh dưới góc độ chuyên môn và người bệnh cũng đã hài lòng.&nbsp;<br>
